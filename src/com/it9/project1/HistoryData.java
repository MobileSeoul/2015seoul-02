package com.it9.project1;

public class HistoryData {
	
	private String title="";
	private String address="";
	private String infomer="";
	private String indication="";
	private String repoter="";
	private String number="";
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getInfomer() {
		return infomer;
	}
	public void setInfomer(String infomer) {
		this.infomer = infomer;
	}
	public String getIndication() {
		return indication;
	}
	public void setIndication(String indication) {
		this.indication = indication;
	}
	public String getRepoter() {
		return repoter;
	}
	public void setRepoter(String repoter) {
		this.repoter = repoter;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	
	
	
}
